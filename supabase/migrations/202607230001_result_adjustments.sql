begin;

create table if not exists public.evaluation_result_adjustments (
  id bigint generated by default as identity primary key,
  cycle_id bigint not null references public.evaluation_cycles(id) on delete cascade,
  target_id bigint not null references public.users(id) on delete cascade,
  raw_score numeric(5,2) not null check (raw_score between 0 and 100),
  final_score numeric(5,2) not null check (final_score between 0 and 100),
  final_grade text not null check (final_grade in ('EX','S','A','B','C','D')),
  reason text not null check (length(trim(reason)) >= 10),
  adjusted_by uuid references auth.users(id) on delete set null,
  adjusted_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (cycle_id, target_id)
);

alter table public.ai_evaluation_reports
  add column if not exists analysis_mode text not null default 'scored'
    check (analysis_mode in ('scored', 'qualitative_only'));

alter table public.evaluation_result_adjustments enable row level security;
revoke all privileges on table public.evaluation_result_adjustments from anon;
revoke insert, update, delete on table public.evaluation_result_adjustments from authenticated;
grant select on table public.evaluation_result_adjustments to authenticated;

drop policy if exists result_adjustments_read_privileged on public.evaluation_result_adjustments;
create policy result_adjustments_read_privileged
on public.evaluation_result_adjustments for select to authenticated
using (public.current_user_is_privileged());

create index if not exists evaluation_result_adjustments_target_cycle_idx
  on public.evaluation_result_adjustments (target_id, cycle_id);

commit;
