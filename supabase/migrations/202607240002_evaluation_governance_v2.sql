begin;

-- Immutable per-cycle personnel context used to calculate relative-grade cohorts.
create table if not exists public.evaluation_cohort_snapshots (
  id bigint generated by default as identity primary key,
  cycle_id bigint not null references public.evaluation_cycles(id) on delete cascade,
  target_id bigint not null references public.users(id) on delete restrict,
  cohort_key text not null check (length(trim(cohort_key)) > 0),
  company text,
  dept text,
  workplace text,
  role text,
  employee_type text,
  profile_snapshot jsonb not null default '{}'::jsonb,
  captured_by uuid references auth.users(id) on delete set null,
  captured_at timestamptz not null default now(),
  unique (cycle_id, target_id)
);

create table if not exists public.evaluation_grade_allocations (
  id bigint generated by default as identity primary key,
  cycle_id bigint not null references public.evaluation_cycles(id) on delete cascade,
  cohort_key text not null check (length(trim(cohort_key)) > 0),
  grade text not null check (grade in ('S', 'A', 'B', 'C', 'D')),
  allocation_count integer not null check (allocation_count >= 0),
  allocation_ratio numeric(5,4) not null check (allocation_ratio between 0 and 1),
  allocated_by uuid references auth.users(id) on delete set null,
  allocated_at timestamptz not null default now(),
  unique (cycle_id, cohort_key, grade)
);

-- Preserve the existing adjustment record while recording each governance stage.
alter table public.evaluation_result_adjustments
  add column if not exists workflow_status text not null default 'draft',
  add column if not exists first_stage_by uuid references auth.users(id) on delete set null,
  add column if not exists first_stage_at timestamptz,
  add column if not exists second_stage_by uuid references auth.users(id) on delete set null,
  add column if not exists second_stage_at timestamptz;

do $$
begin
  if not exists (
    select 1 from pg_constraint
    where conrelid = 'public.evaluation_result_adjustments'::regclass
      and conname = 'evaluation_result_adjustments_workflow_status_check'
  ) then
    alter table public.evaluation_result_adjustments
      add constraint evaluation_result_adjustments_workflow_status_check
      check (workflow_status in ('draft', 'first_stage_adjusted', 'second_stage_adjusted', 'rejected'));
  end if;
end $$;

create table if not exists public.evaluation_adjustment_workflow_audit (
  id bigint generated by default as identity primary key,
  adjustment_id bigint not null references public.evaluation_result_adjustments(id) on delete cascade,
  cycle_id bigint not null references public.evaluation_cycles(id) on delete cascade,
  target_id bigint not null references public.users(id) on delete restrict,
  stage smallint not null check (stage in (1, 2)),
  actor_role text not null check (actor_role in ('admin', 'executive')),
  action text not null check (action in ('adjusted', 'approved', 'rejected', 'cancelled')),
  previous_score numeric(5,2) check (previous_score between 0 and 100),
  next_score numeric(5,2) check (next_score between 0 and 100),
  reason text not null check (length(trim(reason)) >= 10),
  acted_by uuid references auth.users(id) on delete set null,
  acted_at timestamptz not null default now(),
  check (
    (stage = 1 and actor_role = 'admin')
    or (stage = 2 and actor_role = 'executive')
  )
);

-- Approval is cycle-scoped so publication can be gated independently of scores.
alter table public.evaluation_cycles
  add column if not exists internal_approval_required boolean not null default true,
  add column if not exists internal_approval_status text not null default 'not_requested',
  add column if not exists internal_approval_completed_at timestamptz,
  add column if not exists result_gate_open boolean not null default false;

do $$
begin
  if not exists (
    select 1 from pg_constraint
    where conrelid = 'public.evaluation_cycles'::regclass
      and conname = 'evaluation_cycles_internal_approval_status_check'
  ) then
    alter table public.evaluation_cycles
      add constraint evaluation_cycles_internal_approval_status_check
      check (internal_approval_status in ('not_requested', 'requested', 'approved', 'rejected'));
  end if;
end $$;

create table if not exists public.evaluation_cycle_approval_requests (
  id bigint generated by default as identity primary key,
  cycle_id bigint not null references public.evaluation_cycles(id) on delete cascade,
  request_status text not null default 'requested'
    check (request_status in ('requested', 'approved', 'rejected', 'cancelled')),
  requested_by uuid references auth.users(id) on delete set null,
  requested_at timestamptz not null default now(),
  decided_by uuid references auth.users(id) on delete set null,
  decided_at timestamptz,
  decision_note text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index if not exists evaluation_cycle_approval_requests_open_uidx
  on public.evaluation_cycle_approval_requests (cycle_id)
  where request_status = 'requested';

create table if not exists public.evaluation_cycle_approval_audit (
  id bigint generated by default as identity primary key,
  approval_request_id bigint references public.evaluation_cycle_approval_requests(id) on delete set null,
  cycle_id bigint not null references public.evaluation_cycles(id) on delete cascade,
  action text not null check (action in ('requested', 'approved', 'rejected', 'cancelled', 'gate_opened', 'gate_closed')),
  note text,
  acted_by uuid references auth.users(id) on delete set null,
  acted_at timestamptz not null default now()
);

-- A stable idempotency key makes repeated mail requests observable but harmless.
create table if not exists public.evaluation_mail_dispatch_audit (
  id bigint generated by default as identity primary key,
  cycle_id bigint references public.evaluation_cycles(id) on delete set null,
  target_id bigint references public.users(id) on delete set null,
  recipient_email text not null,
  mail_kind text not null check (mail_kind in ('grade_notice', 'password_reset')),
  idempotency_key text not null check (length(trim(idempotency_key)) > 0),
  dispatch_status text not null default 'queued'
    check (dispatch_status in ('queued', 'sent', 'failed')),
  provider_message_id text,
  requested_by uuid references auth.users(id) on delete set null,
  requested_at timestamptz not null default now(),
  dispatched_at timestamptz,
  error_message text,
  unique (idempotency_key)
);

-- Existing category and target_track columns remain canonical. Audience scopes them further.
alter table public.evaluation_questions
  add column if not exists audience text not null default 'all';

do $$
begin
  if not exists (
    select 1 from pg_constraint
    where conrelid = 'public.evaluation_questions'::regclass
      and conname = 'evaluation_questions_audience_check'
  ) then
    alter table public.evaluation_questions
      add constraint evaluation_questions_audience_check
      check (audience in ('all', 'internal', 'exchange', 'leadership'));
  end if;
end $$;

alter table public.matchings
  add column if not exists relationship_type text not null default 'internal';

do $$
begin
  if not exists (
    select 1 from pg_constraint
    where conrelid = 'public.matchings'::regclass
      and conname = 'matchings_relationship_type_check'
  ) then
    alter table public.matchings
      add constraint matchings_relationship_type_check
      check (relationship_type in ('internal', 'exchange', 'leadership'));
  end if;
end $$;

create index if not exists evaluation_cohort_snapshots_cycle_cohort_idx
  on public.evaluation_cohort_snapshots (cycle_id, cohort_key, target_id);
create index if not exists evaluation_grade_allocations_cycle_cohort_idx
  on public.evaluation_grade_allocations (cycle_id, cohort_key);
create index if not exists evaluation_adjustment_workflow_audit_adjustment_idx
  on public.evaluation_adjustment_workflow_audit (adjustment_id, acted_at desc);
create index if not exists evaluation_adjustment_workflow_audit_cycle_target_idx
  on public.evaluation_adjustment_workflow_audit (cycle_id, target_id, acted_at desc);
create index if not exists evaluation_cycle_approval_audit_cycle_idx
  on public.evaluation_cycle_approval_audit (cycle_id, acted_at desc);
create index if not exists evaluation_mail_dispatch_audit_target_idx
  on public.evaluation_mail_dispatch_audit (target_id, requested_at desc);
create index if not exists evaluation_mail_dispatch_audit_status_idx
  on public.evaluation_mail_dispatch_audit (dispatch_status, requested_at);
create index if not exists evaluation_questions_audience_track_category_idx
  on public.evaluation_questions (cycle_id, audience, target_track, category);
create index if not exists matchings_cycle_relationship_idx
  on public.matchings (cycle_id, relationship_type, target_id);

-- All writes are server-side (service_role); RLS protects audit and workflow data.
alter table public.evaluation_cohort_snapshots enable row level security;
alter table public.evaluation_grade_allocations enable row level security;
alter table public.evaluation_adjustment_workflow_audit enable row level security;
alter table public.evaluation_cycle_approval_requests enable row level security;
alter table public.evaluation_cycle_approval_audit enable row level security;
alter table public.evaluation_mail_dispatch_audit enable row level security;

revoke all privileges on table public.evaluation_cohort_snapshots,
  public.evaluation_grade_allocations,
  public.evaluation_adjustment_workflow_audit,
  public.evaluation_cycle_approval_requests,
  public.evaluation_cycle_approval_audit,
  public.evaluation_mail_dispatch_audit
from anon, authenticated;

grant select, insert, update, delete on table public.evaluation_cohort_snapshots,
  public.evaluation_grade_allocations,
  public.evaluation_adjustment_workflow_audit,
  public.evaluation_cycle_approval_requests,
  public.evaluation_cycle_approval_audit,
  public.evaluation_mail_dispatch_audit
to service_role;
grant usage, select on sequence public.evaluation_cohort_snapshots_id_seq,
  public.evaluation_grade_allocations_id_seq,
  public.evaluation_adjustment_workflow_audit_id_seq,
  public.evaluation_cycle_approval_requests_id_seq,
  public.evaluation_cycle_approval_audit_id_seq,
  public.evaluation_mail_dispatch_audit_id_seq
to service_role;

drop policy if exists evaluation_cohort_snapshots_read_privileged on public.evaluation_cohort_snapshots;
create policy evaluation_cohort_snapshots_read_privileged
  on public.evaluation_cohort_snapshots for select to authenticated
  using (public.current_user_is_privileged());

drop policy if exists evaluation_grade_allocations_read_privileged on public.evaluation_grade_allocations;
create policy evaluation_grade_allocations_read_privileged
  on public.evaluation_grade_allocations for select to authenticated
  using (public.current_user_is_privileged());

drop policy if exists evaluation_adjustment_workflow_audit_read_privileged on public.evaluation_adjustment_workflow_audit;
create policy evaluation_adjustment_workflow_audit_read_privileged
  on public.evaluation_adjustment_workflow_audit for select to authenticated
  using (public.current_user_is_privileged());

drop policy if exists evaluation_cycle_approval_requests_read_privileged on public.evaluation_cycle_approval_requests;
create policy evaluation_cycle_approval_requests_read_privileged
  on public.evaluation_cycle_approval_requests for select to authenticated
  using (public.current_user_is_privileged());

drop policy if exists evaluation_cycle_approval_audit_read_privileged on public.evaluation_cycle_approval_audit;
create policy evaluation_cycle_approval_audit_read_privileged
  on public.evaluation_cycle_approval_audit for select to authenticated
  using (public.current_user_is_privileged());

drop policy if exists evaluation_mail_dispatch_audit_read_privileged on public.evaluation_mail_dispatch_audit;
create policy evaluation_mail_dispatch_audit_read_privileged
  on public.evaluation_mail_dispatch_audit for select to authenticated
  using (public.current_user_is_privileged());

commit;
