create or replace function public.governance_force_close_cycle(
  p_cycle_id bigint, p_reason text, p_actor_id uuid
)
returns jsonb language plpgsql security definer set search_path = public, pg_temp
as $$
declare v_cycle public.evaluation_cycles; v_reason text;
begin
  perform public.assert_cycle_governance_actor(p_actor_id, true);
  v_reason := public.assert_cycle_governance_reason(p_reason);
  select * into v_cycle from public.evaluation_cycles where id = p_cycle_id for update;
  if not found then raise exception 'Evaluation cycle not found'; end if;
  if v_cycle.status not in ('진행중', '일시정지') then raise exception 'Only an active or paused cycle can be force-closed'; end if;
  perform set_config('app.cycle_status_transition', 'force_close', true);
  update public.evaluation_cycles
  set status = '마감/보관됨', closed_by = p_actor_id, closed_at = now(),
      force_closed_by = p_actor_id, force_closed_at = now(), force_close_reason = v_reason,
      results_published = false, result_gate_open = false, updated_at = now()
  where id = p_cycle_id;
  insert into public.evaluation_cycle_governance_audit
    (cycle_id, cycle_id_snapshot, cycle_name, action, reason, previous_status, next_status, acted_by)
  values (p_cycle_id, p_cycle_id, v_cycle.name, 'force_closed', v_reason, v_cycle.status, '마감/보관됨', p_actor_id);
  return jsonb_build_object(
    'cycle_id', p_cycle_id, 'status', '마감/보관됨',
    'force_closed', true, 'results_published', false,
    'message', 'Force-closed cycles are retained without a publishable result snapshot'
  );
end $$;

create or replace function public.governance_cancel_cycle(
  p_cycle_id bigint, p_reason text, p_actor_id uuid, p_hard_delete boolean default false
)
returns jsonb language plpgsql security definer set search_path = public, pg_temp
as $$
declare v_cycle public.evaluation_cycles; v_reason text; v_submitted integer;
begin
  perform public.assert_cycle_governance_actor(p_actor_id, true);
  v_reason := public.assert_cycle_governance_reason(p_reason);
  select * into v_cycle from public.evaluation_cycles where id = p_cycle_id for update;
  if not found then raise exception 'Evaluation cycle not found'; end if;
  if v_cycle.status = '마감/보관됨' then raise exception 'Historical cycles cannot be cancelled again'; end if;
  select count(*)::integer into v_submitted from public.evaluations where cycle_id = p_cycle_id;
  if p_hard_delete then
    if v_submitted > 0 then raise exception 'A cycle with submitted evaluations must be retained as a cancelled archive'; end if;
    insert into public.evaluation_cycle_governance_audit
      (cycle_id, cycle_id_snapshot, cycle_name, action, reason, previous_status, next_status, acted_by)
    values (p_cycle_id, p_cycle_id, v_cycle.name, 'hard_deleted', v_reason, v_cycle.status, null, p_actor_id);
    delete from public.evaluation_cycles where id = p_cycle_id;
    return jsonb_build_object('cycle_id', p_cycle_id, 'deleted', true);
  end if;
  perform set_config('app.cycle_status_transition', 'cancel', true);
  update public.evaluation_cycles
  set status = '마감/보관됨', closed_by = p_actor_id, closed_at = now(),
      cancelled_by = p_actor_id, cancelled_at = now(), cancellation_reason = v_reason,
      results_published = false, result_gate_open = false, updated_at = now()
  where id = p_cycle_id;
  update public.evaluation_cycle_approval_requests
  set request_status = 'cancelled', decided_by = p_actor_id, decided_at = now(),
      decision_note = v_reason, updated_at = now()
  where cycle_id = p_cycle_id and request_status = 'requested';
  insert into public.evaluation_cycle_governance_audit
    (cycle_id, cycle_id_snapshot, cycle_name, action, reason, previous_status, next_status, acted_by)
  values (p_cycle_id, p_cycle_id, v_cycle.name, 'cancelled', v_reason, v_cycle.status, '마감/보관됨', p_actor_id);
  return jsonb_build_object('cycle_id', p_cycle_id, 'status', '마감/보관됨', 'cancelled', true, 'submitted_evaluations', v_submitted);
end $$;

revoke all on function public.assert_cycle_governance_actor(uuid, boolean),
  public.assert_cycle_governance_reason(text),
  public.governance_pause_cycle(bigint, text, uuid),
  public.governance_resume_cycle(bigint, text, uuid),
  public.governance_force_close_cycle(bigint, text, uuid),
  public.governance_cancel_cycle(bigint, text, uuid, boolean)
from public;

grant execute on function public.governance_pause_cycle(bigint, text, uuid),
  public.governance_resume_cycle(bigint, text, uuid),
  public.governance_force_close_cycle(bigint, text, uuid),
  public.governance_cancel_cycle(bigint, text, uuid, boolean)
to authenticated;

commit;
